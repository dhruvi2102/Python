#ARITHMETIC OPERATORS

a=10
b=2
print(a%b)

#ASSIGNMENT OPERATORS

x=10
x+=10    #x=x+10
x-=100   #x=x-100
x*=20    #x=x*20

print(x)

#RELATIONAL OPERATORS

x=10
y=20

print(x==y)
print(x>y)
print(x<y)
print(x>=y)
print(x<=y)
print(x!=y)

#LOGICAL OPERATORS

x=10

print(x>5 and x<20)
print(x>5 or x>20)
print(not(x<5 and x>20))
